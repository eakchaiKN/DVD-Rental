    void display(String sql) {
        model1 = (DefaultTableModel) cusTable.getModel();
        model1.setRowCount(0);
        con = ConnectDB.getConnection();
        int i = 0;
        //picture.setIcon(new ImageIcon(img));
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                String id = String.valueOf(rs.getInt("cus_id"));
                String fname = rs.getString("cus_fname");
                String lname = rs.getString("cus_lname");
                String age = String.valueOf(rs.getString("cus_age"));
                String gender = rs.getString("cus_gender");
                String address = rs.getString("cus_address");
                model1.addRow(new Object[]{fname, lname, age, gender, address});
                i++;
                //System.out.println(num+" : "+fname);  
            }
            if (i == 0) {
                JOptionPane.showMessageDialog(null, "Not Result");
                display(sql);
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                con.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

