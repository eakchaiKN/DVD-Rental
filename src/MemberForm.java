    private void newBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newBActionPerformed
        // TODO add your handling code here:
        Member m = new Member();
        //int id = Integer.parseInt(idText.getText());
        String fname = fnameText.getText();
        String lname = lnameText.getText();
        String img = filePath;
        String gender = genderBox.getSelectedItem().toString();
        String addr = addressText.getText();
        int age = 0;
        if (ageText.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Invalid Value");
        } else {
            age = Integer.parseInt(ageText.getText());
            if (fname.equals("") || lname.equals("") || age < 15 || age > 150 || addr.equals("")) {
                JOptionPane.showMessageDialog(null, "Invalid Value");
            } else {
                m.newMember(fname, lname, img, age, gender, addr);
                String sql = "select * from customers";
                display(sql);
            }
        }
        if (img.equals("")) {
            img = "defaultPic.png";
        }


    }//GEN-LAST:event_newBActionPerformed
