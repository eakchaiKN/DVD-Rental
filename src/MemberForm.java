    private void cusTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cusTableMouseClicked
        // TODO add your handling code here:
        selectRow = cusTable.getSelectedRow();
        con = ConnectDB.getConnection();
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM customers");
            int i = 0;
            while (rs.next()) {
                if (selectRow == i) {
                    selectId = rs.getInt("cus_id");
                    String id = String.valueOf(rs.getInt("cus_id"));
                    String fname = rs.getString("cus_fname");
                    String lname = rs.getString("cus_lname");
                    String age = String.valueOf(rs.getString("cus_age"));
                    String gender = rs.getString("cus_gender");
                    String address = rs.getString("cus_address");

                    idText.setText(id);
                    fnameText.setText(fname);
                    lnameText.setText(lname);
                    ageText.setText(age);
                    genderBox.setSelectedItem(gender);
                    addressText.setText(address);
                    filePath = rs.getString("img");
                    if (filePath.equals("")) {
                        img = ImageIO.read(getClass().getResource("/image/defaultPic.png"));
                    } else {
                        img = ImageIO.read(getClass().getResource("/image/" + filePath));
                    }
                    picture.setIcon(new ImageIcon(img));
                }
                i++;
                //System.out.println(i);
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            con = ConnectDB.closeConnection();
            System.out.println(selectRow);
        }

    }//GEN-LAST:event_cusTableMouseClicked

